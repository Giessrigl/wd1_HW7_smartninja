mood = input("what's your mood: ")

if mood == "happy":
    print("It is great to see you happy!")

elif mood == "nervous":
    print("Take a deep breath 3 times.")

elif mood == "sad":
    print("Cheer up, mate!")

elif mood == "excited":
    print("Keep calm!")

elif mood == "relaxed":
    print("I don´t know what to say^^")

else:
    print("I don´t recognize this mood")
